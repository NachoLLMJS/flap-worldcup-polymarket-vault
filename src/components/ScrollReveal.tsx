import { motion } from 'framer-motion';
import type { ReactNode } from 'react';

/** Reveals content on scroll into view (fade + rise). Optional stagger via
 *  nested ScrollReveal children. Respects reduced motion through framer. */
export function ScrollReveal({
  children,
  delay = 0,
  y = 28,
  className,
}: {
  children: ReactNode;
  delay?: number;
  y?: number;
  className?: string;
}) {
  return (
    <motion.div
      className={className}
      initial={{ opacity: 0, y }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, margin: '-80px' }}
      transition={{ duration: 0.6, delay, ease: [0.22, 1, 0.36, 1] }}
    >
      {children}
    </motion.div>
  );
}
